-- Таблица FACULTY
CREATE TABLE FACULTY (
    FACULTY VARCHAR(50) PRIMARY KEY,
    FACULTY_NAME VARCHAR(100) NOT NULL
);

-- Таблица PULPIT
CREATE TABLE PULPIT (
    PULPIT VARCHAR(50) PRIMARY KEY,
    PULPIT_NAME VARCHAR(100) NOT NULL,
    FACULTY VARCHAR(50) NOT NULL,
    FOREIGN KEY (FACULTY) REFERENCES FACULTY(FACULTY) ON DELETE CASCADE
);

-- Таблица TEACHER
CREATE TABLE TEACHER (
    TEACHER VARCHAR(50) PRIMARY KEY,
    TEACHER_NAME VARCHAR(100) NOT NULL,
    PULPIT VARCHAR(50) NOT NULL,
    FOREIGN KEY (PULPIT) REFERENCES PULPIT(PULPIT) ON DELETE CASCADE
);

-- Таблица SUBJECT
CREATE TABLE SUBJECT (
    SUBJECT VARCHAR(50) PRIMARY KEY,
    SUBJECT_NAME VARCHAR(100) NOT NULL,
    PULPIT VARCHAR(50) NOT NULL,
    FOREIGN KEY (PULPIT) REFERENCES PULPIT(PULPIT) ON DELETE CASCADE
);

-- Таблица AUDITORIUM_TYPE
CREATE TABLE AUDITORIUM_TYPE (
    AUDITORIUM_TYPE VARCHAR(50) PRIMARY KEY,
    AUDITORIUM_TYPENAME VARCHAR(100) NOT NULL
);

-- Таблица AUDITORIUM
CREATE TABLE AUDITORIUM (
    AUDITORIUM_NAME VARCHAR(50) PRIMARY KEY,
    AUDITORIUM_CAPACITY INT NOT NULL,
    AUDITORIUM_TYPE VARCHAR(50) NOT NULL,
    FOREIGN KEY (AUDITORIUM_TYPE) REFERENCES AUDITORIUM_TYPE(AUDITORIUM_TYPE) ON DELETE CASCADE
);

